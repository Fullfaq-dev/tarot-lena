from datetime import date

SIGNS = [
    ("Козерог", "♑", (1, 1), (1, 19)),
    ("Водолей", "♒", (1, 20), (2, 18)),
    ("Рыбы", "♓", (2, 19), (3, 20)),
    ("Овен", "♈", (3, 21), (4, 19)),
    ("Телец", "♉", (4, 20), (5, 20)),
    ("Близнецы", "♊", (5, 21), (6, 20)),
    ("Рак", "♋", (6, 21), (7, 22)),
    ("Лев", "♌", (7, 23), (8, 22)),
    ("Дева", "♍", (8, 23), (9, 22)),
    ("Весы", "♎", (9, 23), (10, 22)),
    ("Скорпион", "♏", (10, 23), (11, 21)),
    ("Стрелец", "♐", (11, 22), (12, 21)),
    ("Козерог", "♑", (12, 22), (12, 31)),
]


def zodiac_sign(birth: date) -> tuple[str, str]:
    md = birth.month * 100 + birth.day
    for name, emoji, (sm, sd), (em, ed) in SIGNS:
        start = sm * 100 + sd
        end = em * 100 + ed
        if start <= md <= end:
            return name, emoji
    return "Рыбы", "♓"
